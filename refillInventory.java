    private void comboBoxMagic(String item) {
        if (dbItems.getSelectedItem() == "-Select Item-") {
            numInStock.disable();
            reorderAmt.disable();
            name.setText("");
            upc.setText("");
            reorderAmt.setText("");
            numInStock.setText("");
        } else {
            DBstuff db = new DBstuff();
            ArrayList<Item> list = db.search(item);
            name.setText(list.get(0).getName());
            upc.setText(list.get(0).getUpc());
            numInStock.setText(list.get(0).getNumInStock() + "");
            reorderAmt.setText(list.get(0).getReorderAmount() + "");
            numInStock.enable();
            reorderAmt.enable();
        }
    }

