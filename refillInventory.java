    private void reformatComboBox() {
        dbItems.removeAllItems();
        DBstuff db = new DBstuff();
        ArrayList<String> list = db.listItems();
        dbItems.addItem("-Select Item-");
        for (String s : list) {
            dbItems.addItem(s);
        }
    }

